.MODEL SMALL
.STACK 100H
.DATA
    MSG1    DB 10,13,'NUMBER IN HEX:- $'
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    MOV AH, 09H
    LEA DX, MSG1
    INT 21H
    
    MOV BX, 0
    ADD BX, 1A22H

    MOV CX, 4H

START:
    PUSH CX

    MOV CL, 4
    ROL BX, CL
    POP CX
    MOV DL, BL
    AND DL, 0FH
    ADD DL, 30H
    CMP DL, 39H
    JLE PRINT
    ADD DL, 07H

PRINT:
    MOV AH, 02H
    INT 21H

    LOOP START
    
    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN